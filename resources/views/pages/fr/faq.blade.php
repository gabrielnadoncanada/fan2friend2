            <x-accordion index="10"
                         title="Je n'ai pas pu assister à l'événement, comment puis-je obtenir un remboursement?">
                <p>Malheureusement, tout comme vous ne serez pas remboursé si vous n'assistez pas à un
                    concert ou à un film pour lequel vous avez déjà acheté des billets, nous n'offrons pas
                    de remboursement pour les événements manqués.
                </p>
                <p> La seule exception est si l'horaire de
                    l'événement a changé et que vous n'avez pas pu vous rendre à la nouvelle heure.</p>

                <p>Dans ce cas particulier, vous pouvez demander un remboursement dans les 24 heures suivant le
                    changement d'horaire. Passé ce délai, aucun remboursement n'est possible. Bien que nous
                    souhaitions être équitables envers tout le monde, ce délai de 24 heures est destiné à
                    préserver l'intégrité de l'événement.</p>
            </x-accordion>
