            <x-accordion index="3" title="Exemples de questions à poser:">
                <ul>
                    <li>
                        <p>Vous pouvez poser une question simple, comme "Qu'est-ce qui vous a poussé à
                            faire ce film, à créer cet album ou à écrire ce livre?"</p>
                    </li>
                    <li>
                        <p>Vous pouvez aussi poser une question plus spécifique, qui peut être obscure, pour montrer
                            que vous êtes un vrai fan. Par exemple, vous pouvez demander : "Pourquoi terminez-vous
                            toujours vos chansons par deux applaudissements ?" ou "Qu'avez-vous ressenti en
                            travaillant avec Robert Downey Jr. dans Avengers?</p>
                    </li>
                    <li>
                        <p>Rappelez-vous pourquoi vous êtes là. En ayant déjà à
                            l'esprit des interactions significatives, vous vous abstiendrez de crier "OMG JE T'AIME
                            À LA TÉLÉ", alors que vous souhaiteriez peut-être avoir une conversation plus
                            approfondie.</p>
                    </li>
                </ul>
