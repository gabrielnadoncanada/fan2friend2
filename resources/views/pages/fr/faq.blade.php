            <x-accordion index="4" title="Arrivez tôt et mettez-vous à l'aise"
            >
                <p>Essayez d'entrer dans votre salle de rencontre au moins une demi-heure avant l'heure prévue
                    de votre événement. La file d'attente pour la rencontre et l'accueil est la suivante : premier
                    arrivé, premier servi. Cela vous permettra également d'avoir du temps si vous rencontrez des
                    problèmes avec votre connexion internet.</p>
                <p>Assurez-vous d'être dans une pièce calme et sans distraction. Vous ne voulez pas que
                    quelque chose vienne interrompre votre expérience une fois qu'elle a commencé.</p>
            </x-accordion>
