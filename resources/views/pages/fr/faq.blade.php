            >
                <p>Il n'y a jamais de situation où il est acceptable de manquer de respect. Traitez l'autre
                    partie avec dignité, comme vous le feriez pour n'importe quelle autre personne. N'oubliez pas
                    qu'il s'agit d'une personne comme vous. Malheureusement, il y a un million de choses qui peuvent
                    mal tourner dans une situation de rencontre et d'accueil.

                <p> C'est la vie. L'autre partie peut être
                    en retard à l'événement ou il peut y avoir un problème de connexion Internet. Nous voulons que
                    vous ayez la meilleure expérience possible. </p>
                <p> Si vous n'êtes toujours pas satisfait, n'hésitez pas
                    à nous contacter. Mais ne gâchez pas votre événement en vous frustrant et ne vous plaignez pas
                    lors de votre rencontre : ils essaient simplement de vous faire vivre une expérience
                    inoubliable.</p>


