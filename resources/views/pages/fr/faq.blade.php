            >
                <p>
                    Pour éviter d'être débordé lors de votre rencontre, vous pouvez prévoir
                    quelques questions à l'avance. </p>

                <p>Votre temps étant limité, veillez à
                    ne pas poser de questions nécessitant de longues réponses. Essayez de choisir une question
                    principale que vous aimeriez poser et/ou une pensée que vous aimeriez partager.

                <p>Cela vous
                    permettra de rassembler vos idées et de vous préparer.</p>

